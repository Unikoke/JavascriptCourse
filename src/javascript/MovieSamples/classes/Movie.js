/**
 * Represents a movie with its associated information.
 */
class Movie {
    /**
     * The unique identifier for the film.
     * @type {number}
     */
    filmId;
   /**
    * The name of the movie.
    * @type {string}
    */
   name;
   /**
    * The genres of the movie.
    * @type {string|string[]}
    */
   genres;
   /**
    * The actors in the movie.
    * @type {string|string[]}
    */
   actors;
}
